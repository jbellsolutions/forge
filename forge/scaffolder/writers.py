_CLAUDE_SUBAGENT = """---
name: {name}
description: {role} — {description_short}
tools: {tools_list}
---

{instructions}

## Provider hint
This agent was designed for the `{profile}` profile in forge. When running
inside Claude Code (Anthropic), you can ignore the profile field — the host
session's model is used. The name + role + instructions above are what
matter.

## Source
Generated by `forge new` from this description:

> {description}
"""


def write_claude_subagents(design: SwarmDesign, repo_root: Path) -> list[Path]:
    """Write `<repo>/.claude/agents/<name>.md` per agent. Drop-in for any
    Claude Code session."""
    repo_root = Path(repo_root)
    agents_dir = repo_root / ".claude" / "agents"
    agents_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    short_desc = (design.description[:80] + "…") if len(design.description) > 80 else design.description
    for spec in design.agents:
        path = agents_dir / f"{design.name}_{spec.name}.md"
        path.write_text(_CLAUDE_SUBAGENT.format(
            name=f"{design.name}_{spec.name}",
            role=spec.role,
            description_short=short_desc,
            description=design.description,
            tools_list=", ".join(spec.tools) if spec.tools else "(inherits parent)",
            instructions=spec.instructions,
            profile=spec.profile,
        ))
        written.append(path)
    # README so the user knows where these came from
    readme = agents_dir / f"{design.name}.README.md"
    readme.write_text(
        f"# {design.name} (Claude Code subagent bundle)\n\n"
        f"{design.description}\n\n"
        "These markdown files are Claude Code agent definitions. "
        "Invoke them in Claude Code via `/agents` or by mentioning the agent "
        "name. They were generated by `forge new` — re-run that to refresh.\n"
    )
    written.append(readme)
    return written


# ---------------------------------------------------------------------------
# 3. Dashboard — POST a propose_start_project PendingAction
# ---------------------------------------------------------------------------

def propose_dashboard_action(
    design: SwarmDesign, dashboard_url: str, sync_token: str,
    *, transport=None,
) -> str:
    """POST /sync/propose-design with a custom-template payload. Returns the
    PendingAction id for the user to Approve in the workspace.

    Requires the dashboard server to support `POST /sync/propose-design`
    (added alongside this scaffolder).
    """
    body = json.dumps({
        "kind": "start_project",
        "payload": {
            "name": design.name,
            "template": "custom",
            "description": design.description,
            "design_json": design.to_json(),
        },
    }).encode("utf-8")
    headers = {
        "Content-Type": "application/json",
        "X-Forge-Sync-Token": sync_token,
    }
    target = dashboard_url.rstrip("/") + "/sync/propose-design"
    if transport is not None:
        return transport(target, body, headers).get("id", "")
    req = urllib.request.Request(target, data=body, headers=headers, method="POST")
    with urllib.request.urlopen(req, timeout=30) as resp:  # nosec — own server
        data = json.loads(resp.read().decode("utf-8"))
    return data.get("id", "")
