def write_terminal_project(design: SwarmDesign, root: Path) -> list[Path]:
    """Write `examples/<name>/{run.py, README.md}` and an optional heartbeat."""
    root = Path(root)
    target = root / "examples" / design.name
    target.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []

    agents_repr = json.dumps(
        [{"name": a.name, "role": a.role, "instructions": a.instructions,
          "profile": a.profile, "tools": a.tools} for a in design.agents],
        indent=4,
    )
    (target / "run.py").write_text(_TERMINAL_RUN_PY.format(
        description=design.description.replace('"""', '\\"\\"\\"'),
        name=design.name,
        agents_repr=agents_repr,
    ))
    written.append(target / "run.py")

    agents_md = "\n".join(
        f"- **{a.name}** ({a.role}, `{a.profile}`)" for a in design.agents
    )
    schedule_md = (
        f"## Schedule\n\nThis swarm runs on cron `{design.schedule}` via the heartbeat "
        f"at `examples/{design.name}/heartbeats/run.md`."
        if design.schedule else
        "## Schedule\n\nOn-demand only. Add a heartbeat under `heartbeats/` to schedule."
    )
    (target / "README.md").write_text(_TERMINAL_README.format(
        name=design.name, description=design.description,
        agents_md=agents_md, schedule_md=schedule_md,
    ))
    written.append(target / "README.md")

    if design.schedule:
        hb_dir = target / "heartbeats"
        hb_dir.mkdir(exist_ok=True)
        hb_path = hb_dir / "run.md"
        hb_path.write_text(
            f"---\nschedule: \"{design.schedule}\"\nagent: {design.name}\n---\n\n"
            f"# {design.name} heartbeat\n\n"
            f"Runs the swarm on cron `{design.schedule}`.\n\n"
            f"```bash\npython examples/{design.name}/run.py\n```\n"
        )
        written.append(hb_path)

    (target / "design.json").write_text(design.to_json())
    written.append(target / "design.json")

    return written


